package edu.lfa.academy.entity;

import java.sql.Date;


/**
 *
 * @author charm_000
 */
public class Facilator {
    private int id;
    private String firstname, lastname, email;
    private Date addedDate, modifiedDate;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public Date getAddedDate() {
        return addedDate;
    }

    public void setAddedDate(Date addedDate) {
        this.addedDate = addedDate;
    }

    public Date getModifiedDate() {
        return modifiedDate;
    }

    public void setModifiedDate(Date modifiedDate) {
        this.modifiedDate = modifiedDate;
    }

    @Override
    public String toString() {
        return "Facilator{" + "id=" + id + ", firstname=" + firstname + ", lastname=" + lastname + ", email=" + email + ", addedDate=" + addedDate + ", modifiedDate=" + modifiedDate + '}';
    }
    Facilator(){
    }
    
            
}
